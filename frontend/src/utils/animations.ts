// Utility function to create staggered animations
export const createStaggered = (children: number, staggerDelay = 0.1) => ({
  animate: {
    transition: {
      staggerChildren: staggerDelay,
      delayChildren: 0.1,
    },
  },
});

// Utility function for delayed animations
export const withDelay = (animation: Variants, delay: number): Variants => ({
  ...animation,
  animate: {
    ...animation.animate,
    transition: {
      delay,
      duration: 0.5,
      ease: 'easeOut',
    },
  },
});